package gui;

public class Jugador {

	public String name;
	public int equipo;
	public int posX;
	public int posY;
	public String orientacion;

	public Jugador(String name, int equipo, int posX, int posY, String orientacion) {
		this.name = name;
		this.equipo = equipo;
		this.posX = posX;
		this.posY = posY;
		this.orientacion = orientacion;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public int getEquipo() {
		return equipo;
	}

	public void setEquipo(int equipo) {
		this.equipo = equipo;
	}

	public int getPosX() {
		return posX;
	}

	public void setPosX(int posX) {
		this.posX = posX;
	}

	public int getPosY() {
		return posY;
	}

	public void setPosY(int posY) {
		this.posY = posY;
	}

	public String getOrientacion() {
		return orientacion;
	}

	public void setOrientacion(String orientacion) {
		this.orientacion = orientacion;
	}

}
